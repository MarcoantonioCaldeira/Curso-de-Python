num = int(input("Digite um numero de 1 até 7: "))

if num == 1:
    print("Segunda-Feira")
elif num == 2:
    print("Terça-Feira")
elif num == 3:
    print("Quarta-Feira")
elif num == 4:
    print("Quinta-Feira")
elif num == 5:
    print("Sexta-Feira")
elif num == 6:
    print("Sabado")
elif num == 7:
    print("Domingo")
elif num > 7:
    print("NUMERO INVALIDO!!!")
